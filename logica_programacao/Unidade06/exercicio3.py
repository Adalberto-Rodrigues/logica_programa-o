"""Diferencie tuplas de listas e dicionários."""

"""Listas(list)
Coleção ordenada de elementos mutável (pode ser alterada depois de criada).
identificamos como: Colchetes [], Pode conter: Qualquer tipo de dado (misturado se quiser).
Exemplo: frutas = ["maçã", "banana", "uva"]
Suas características:Mantém a ordem dos elementos, permite itens repetidose e são Ideais para qundo precisamos mudar algo.

Tupla(tuple)
Coleção ordenada de elementos imutável (não pode ser alterada depois de criada).
Identificamos como: Parênteses () (ou vírgulas sem parênteses em alguns casos), pode conter: Qualquer tipo de dado.
Exemplo: coordenadas = (10, 20)
Suas características: Mantém a ordem dos elementos, permite itens repetidos e são mais rápida e segura quando os dados não devem mudar.

Dicionário (dict)
Identificamos como: Chaves {} com pares chave: valor.
Exemplo:aluno = {"nome": "Maria", "idade": 20, "nota": 9.5}
Suas características: Acesso rápido por chave, chaves não podem se repetir, ideal para dados relacionados."""