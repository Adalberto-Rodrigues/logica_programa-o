"""Escreva um algoritmo que receba do usuário a base e a altura de um triângulo, calcule e exiba sua área."""
print('Resposta')
base = float(input('digite o tamanho da base:'))
altura = float(input('digite o tamanho da sua altura :'))
area = (base * altura)/2
print(f'a area do triangulo é , {area}m')
